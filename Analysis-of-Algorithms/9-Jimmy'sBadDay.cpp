#include <iostream>
#define MAX 310
using namespace std;
int n;
int m[MAX], t[MAX];

int main(int argc, char const *argv[])
{
    while (true)
    {
        cin >> n;
        if (n == 0)
            break;
        for (int i = 0; i < n; i++)
            cin >> m[i] >> t[i];
                
        
    }
    
    return 0;
}
